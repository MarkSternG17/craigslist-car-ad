		<h4>For this project I will focus on the buyer persona only.</h4>A buyer is typically:
		<ul>
			<li>Looking to buy a car.</li>
			<li>May need a new car or may be looking for a specific car.</li>
			<li>Generally speaking, an auto buyer on Craigslist does not need/want help financing a car.</li>
			<li>Most users prefer an aggregated listing of vehicles before traveling to purchase one.</li>
		</ul>
