		<h4>Once a buyer makes his/her choice, they are presented with several options to initiate a conversation between the buyer and seller.</h4>
			<p>The "reply" button produces a drop-down menu that provides:</p>
		<ul>
			<li> Seller's name</li>
			<li>Phone number</li>
			<li>Email address (either secure through Craigslist or personal</li>
		</ul>
		<p>If the buyer, or anyone browsing the listing for that matter, notices any reason the listing should be taken down, they simply click on the "prohibited" box.
		Craigslist will examine the listing and make the final determination. <strong>Some reasons for clicking the "prohibited" button are:</strong></p>
		<ul>
			<li>A buyer has discovered that the vehicle pictured is not the actual vehicle for sale.</li>
			<li>There is offensive language/profanity within the listing.</li>
			<li>Perhaps a savvy car buyer notices details (on the right of the page) that do not match the specific Year, Make or Model of the vehicle.</li>
		</ul>
